package controlstatement;

public class percentage1 {

	public static void main(String[] args) {
		int m1=87;
		int m2=70;
		int m3=76;
		int scored=m1+m2+m3;
		int total=300;
		float percentage= (float)scored/total*100;
		System.out.println("Percentage:"+percentage);
		System.out.format("%.2f",percentage);

	}


	}


